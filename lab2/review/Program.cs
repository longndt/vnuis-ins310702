using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace review
{
    internal class Program
    {
        static void Main(string[] args)
        {
            //to display text, we must double quotation " "
            Console.WriteLine("Hello world");
            //we must add semicolon (;) at the end of each statement
            Console.WriteLine("We are learning C#");
            //to display number, quotations are optional
            Console.Write(2024);
            //Write: display content to console without creating new line
            //WriteLine: display content and move to new line
            Console.WriteLine(2025);

            //Press CTRL + F5 to run code
        }
    }
}
